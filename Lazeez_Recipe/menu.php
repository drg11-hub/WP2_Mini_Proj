                            <h2 class="mt-0">Elaicheesecake</h2>
                            <p>A delectable, semi-sweet New York style cheesecake, with Graham cracker crust and spiced with Indian cardamoms.</p>
                            <p>
                            Ingredients:</br>
                            1. 120g biscuits</br>
                            2. 60g  melted butter</br>
                            3. 400g creamcheese (Full fat)</br>
                            4. 120g granulated sugar</br>
                            5. 200g sour cream or 100g heavy cream + 100g yogurt</br>
                            6. 150ml heavy cream</br>
                            7. 2 eggs</br>
                            8. 2tbsp corn starch</br>
                            9. 1.5tbsp vanilla extract</br>
                            10.1/4 lemon juice</br>
                            11.200g raspberres</br>
                            12.40g granulated sugar</br>
                            13.1tbsp water</br></br>


                            1. Add the Graham crackers, toasted pecans, salt, sugar and cinnamon to a food processor fitted with a blade attachment. Pulse until broken into a fine meal.</br>

                            2. Drizzle the melted butter in while pulsing. Remove blade and use a spatula to finish mixing in the butter.</br>
                            3. Transfer the crust to the springform pan. Use a tall glass to tamp it down and to the side. The more you compact the crust the better it will cut after baking so give it a nice press. Bake at 350F for about 10 minutes or until it just begins to brown.</br>
                            4. Cream the room temperature cream cheese, sugar and salt together until smooth. I’m using a stand mixer fitted with a paddle attachment but this can be done with a hand mixer as well.</br>
                            5. Add in the room temperature sour cream and beat until completely combined then scrape the bowl down.</br>

                            6. Add the eggs and yolks one at a time allowing each to incorporate before adding the next then toss in that vanilla.</br>
                            7. Sprinkle in the flour while the mixer is running on low. Scrape down the bowl really well and then miix until batter is totally smooth.</br>

                            8. Pour the batter into your partially baked crust. The filling will not really rise during the bake so you can pour to almost the top. I wrapped my pan in foil because I was going to baking in a water bath but it turned out not to be needed.  Bake at 350F for 20 minutes then reduce temperature to 300F and continue baking to 40 minutes. Leave the oven door closed for an hour after baking and then you can remove from the oven. This cake is best chilled so if possible make it a day ahead.

