                        <h2 class="mt-0">Vadonut</h2>
                        <p>A quintessential ConFusion experience, is it a vada or is it a doughnut?</p>
                        <p>
                        1.Urad daal – 1 Cup</br>
                        2.Green chilies – 4</br>
                        3.Ginger – 2inches</br>
                        4.Water – to soak urad daal + 3-4TBS while grinding it</br>
                        5.Oil – For frying</br>
                        6.Shredded coconut or powder – ¼ TSP</br>
                        7.Salt – As per taste</br></br>

                        Soak urad daal in plenty of water overnight.</br>
                        Now take out water and blend the daal into mixer adding green chilies and ginger.</br>
                        Add little water to make grinding process easy but don’t add more.</br>
                        Make smooth paste. It will take about 5 to 8 minutes in mixer.</br>
                        Take out daal paste and place it into off oven with keeping light on.</br>
                        After resting 5-7 hours, take out batter from oven.</br>
                        Now add shredded coconut and salt to it and mix it until 5-7minutes to create air bubbles inside our batter to make fluffy medu vada.</br>
                        Side by side start to heat up oil for frying and start to make medu vada.</br>
                        Take bowl full with water and wet your hand.</br>
                        Take small dumpling of batter into your hand, place thumb in middle of it and give donut shape to it.</br>
                        Slowly flip it to oil.</br>
                        Keep your hand wet all the time while you are placing vada into oil. By that batter will not stick to your hand and it will easily flip in oil.</br>
                        Make all vadas with same way and fry it on medium flame around 2-3minutes.</br>
                        Serve it hot with coconut chutney and sambhar.</br>
                        </p>
