                        <h2 class="mt-0">Uthappizza</h2>
                        <p>A unique combination of Indian Uthappam (pancake) and Italian pizza, topped with Cerignola olives, ripe vine cherry tomatoes, Vidalia onion, Guntur chillies and Buffalo paneer.</p>
                        <!-- <button type="button" class="btn btn-primary" data-toggle="collapse" href="#collapseExample" >View recipe</button> -->
                        
                        <p>
                        Preparation Time: 10 minutes.</br>
                        Cooking Time: 20 minutes.</br>
                        Makes 3 uttapam pizzas. </br></br>

                        1 cup leftover dosa batter</br>
                        6 tbsp pizza sauce</br>
                        1½ tsp oil for greasing</br> 
                        ½ cup thinly sliced onions</br>
                        ½ cup thinly sliced capsicum</br>
                        6 tbsp grated mozzarella cheese</br></br>

                        1. Heat a non-stick tava (griddle) and grease it using ½ tsp of oil.</br>
                        2. Pour a spoonful of the batter and spread in a circular motion to make a 150 mm. (6") uttapam and cook on both the sides till light brown in colour from both the sides.</br>
                        3. Place the uttapam on a clean, dry surface, put 2 tbsp of pizza sauce over it and spread it evenly.</br>
                        4. Sprinkle little onion and capsicum evenly over it.</br>
                        5. Finally sprinkle 2 tbsp of cheese evenly over it.</br>
                        6. Heat a non-stick tava (griddle), place the uttapam pizza on it, cover with a lid and cook on a slow flame for 3 minutes or till the cheese melts.</br>
                        7. Repeat steps 1 to 6 to make 2 more uttapam pizza.</br>
                        8. Cut into 4 equal pieces and serve immediately.</br>
                        
